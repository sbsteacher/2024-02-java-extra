package com.green.javaextra.day01;

public class Variable3 {
    public static void main(String[] args) {
        int n1 = 10;
        System.out.println(n1++);
        System.out.println(n1++);
        System.out.println(n1);

        System.out.println("-----------");

        int n2 = 10;
        System.out.println(++n2);
        System.out.println(++n2);
        System.out.println(n2);

    }
}
